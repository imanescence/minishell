#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (!((c[i] >= '0' && c[i] <= '9') || c[i] == '-'))
			return (0);
		i++;
	}
	return (1);
}
